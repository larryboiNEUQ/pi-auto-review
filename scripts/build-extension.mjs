#!/usr/bin/env node
/**
 * Bundle the single Pi composition entry into committed index.js so startup
 * loads one precompiled ESM graph instead of jiti-transpiling ~100+ .ts files.
 *
 * Host packages stay external (Pi loader aliases). tree-sitter / zod stay
 * external so WASM paths resolve from the install's node_modules.
 */
import * as esbuild from "esbuild";
import { existsSync } from "node:fs";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outfile = join(root, "index.js");
const entry = join(root, "index.ts");

const HOST_EXTERNALS = [
  "@earendil-works/pi-ai",
  "@earendil-works/pi-ai/*",
  "@earendil-works/pi-coding-agent",
  "@earendil-works/pi-tui",
  "@earendil-works/pi-agent-core",
];

const RUNTIME_EXTERNALS = ["web-tree-sitter", "tree-sitter-bash", "zod"];

function resolveTsCandidate(basePath) {
  const candidates = [
    basePath,
    `${basePath}.ts`,
    `${basePath}.js`,
    join(basePath, "index.ts"),
    join(basePath, "index.js"),
  ];
  for (const candidate of candidates) {
    if (existsSync(candidate)) return candidate;
  }
  return `${basePath}.ts`;
}

/** Resolve package.json "imports" subpath patterns like #src/* and #safe/*. */
function hashImportsPlugin() {
  const maps = [
    {
      prefix: "#src/",
      base: join(root, "packages/pi-permission-system/src"),
    },
    {
      prefix: "#safe/",
      base: join(root, "packages/pi-permission-safe-allow/src"),
    },
  ];

  return {
    name: "hash-imports",
    setup(build) {
      build.onResolve({ filter: /^#/ }, (args) => {
        for (const { prefix, base } of maps) {
          if (!args.path.startsWith(prefix)) continue;
          const rel = args.path.slice(prefix.length);
          return { path: resolveTsCandidate(join(base, rel)) };
        }
        return undefined;
      });
    },
  };
}

async function main() {
  await mkdir(dirname(outfile), { recursive: true });

  const result = await esbuild.build({
    absWorkingDir: root,
    entryPoints: [entry],
    outfile,
    bundle: true,
    platform: "node",
    format: "esm",
    target: "node22",
    // No sourcemap in the committed artifact: keeps Git install small and avoids
    // paying to open a large map next to the entry on every process start.
    sourcemap: false,
    minify: true,
    // Keep import.meta.url pointing at the emitted file (tree-sitter WASM resolve).
    banner: {
      js: "/* pi-auto-review precompiled extension entry — generated by scripts/build-extension.mjs */\n",
    },
    external: [...HOST_EXTERNALS, ...RUNTIME_EXTERNALS],
    plugins: [hashImportsPlugin()],
    logLevel: "info",
    write: true,
    metafile: true,
    // Type-only imports and .ts extensions in source paths
    loader: {
      ".ts": "ts",
    },
    tsconfigRaw: {
      compilerOptions: {
        target: "ES2024",
        module: "ESNext",
        moduleResolution: "bundler",
        strict: true,
        skipLibCheck: true,
        esModuleInterop: true,
        resolveJsonModule: true,
      },
    },
  });

  if (result.errors.length > 0) {
    process.exitCode = 1;
    return;
  }

  // Stamp a short provenance footer for operators grepping the artifact.
  const built = await readFile(outfile, "utf8");
  if (!built.includes("pi-auto-review precompiled")) {
    await writeFile(outfile, built, "utf8");
  }

  const bytes = Buffer.byteLength(built);
  const inputs = Object.keys(result.metafile?.inputs ?? {}).length;
  console.log(`Built ${outfile} (${bytes} bytes, ${inputs} input files)`);
}

await main();
